/**
 * swap_ints - Function to swap values of integer.
 * @a: one of the integer to swap.
 * @b: The othe  integer to swap.
 */
void swap_ints(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

